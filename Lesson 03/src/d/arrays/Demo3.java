package d.arrays;

public class Demo3 {
	
	public static void main(String[] args) {
		
		// create an array of String
		String[] names = new String[3];
		
		// populate the array with elements
		names[0] = "Dan";
		names[1] = "Dana";
		names[2] = "Ran";
		
		// print array elements
		for (int i = 0; i < names.length; i++) {
			System.out.println(names[i]);
		}
		
		
	}

}
