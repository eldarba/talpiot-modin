package a;

public class Demo5Operators {
	
	public static void main(String[] args) {
		
		int x = 5;
		x = x + 1; // assignment
		x++; // x = x + 1;
		
		System.out.println(x); // print
		
		System.out.println(++x); // assign then print
		System.out.println(x++);  // print then assign
		
		System.out.println(x); // print
	}

}
