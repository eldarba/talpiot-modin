package c.primitives;

public class Demo3 {

	public static void main(String[] args) {

		// the primitive types for whole numbers
		byte x1 = 127; // smallest integer
		short x2 = 32767; // larger
		int x3 = 2147483647; // larger
		long x4 = 9223372036854775807L; // largest integer
		
		// the primitive types for decimal numbers
		float f1 = 5.6F;
		double f2 = 5.6D;
		
				
	}

}
