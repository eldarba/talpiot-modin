package c.primitives;

public class Demo4 {

	public static void main(String[] args) {

		// Expressions (calculations) results has a type
		// result types depends on the expression elements

		float x1 = 5 + 5L + 3.2F;
		double x2 = 5 + 5L + 3.2;
		long x3 = 5 + 9 + 7L;
		
		System.out.println(x1);
		System.out.println(x2);
		System.out.println(x3);
	}

}
