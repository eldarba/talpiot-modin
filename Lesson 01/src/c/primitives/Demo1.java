package c.primitives;

public class Demo1 {

	public static void main(String[] args) {

		// numeral types
		int x = 10;
		double y = 10.25;
		System.out.println(x);
		System.out.println(y);

		// logical types
		boolean b1 = true;
		boolean b2 = 10 < 100;
		boolean b3 = 10 > 100;
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);

		// characters (text)
		char c1 = 'A';
		System.out.println(c1);
	}

}
