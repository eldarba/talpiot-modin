package d.operators;

public class Demo2 {
	
	public static void main(String[] args) {
		int hour = 22;
		System.out.println(hour % 24);
		hour++; // hour = hour + 1
		System.out.println(hour % 24);
		hour++; // hour = hour + 1
		System.out.println(hour % 24);
		hour++; // hour = hour + 1
		System.out.println(hour % 24);
	}

}
